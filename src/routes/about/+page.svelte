<InfoPage title="About">
  <p>
    Phobos is an online maths visualization tool created by Destructor_Ben, intended for use by
    Maths students in New Zealand studying NCEA or at university.
  </p>

  <br />
  <p>
    Phobos includes visualizations of various mathematical concepts to help visual learners better
    understand maths, since not everyone learns best through textbooks, lectures, or classrooms. One
    key focus of Phobos is to make these visualizations interactive, since simply looking at a
    diagram isn't enough for someone to grasp a concept, but interacting with it can make something
    click. Simply observing is usually not enough.
  </p>

  <br />
  <p>
    Phobos is completely open source, and the code is available on <a
      href="https://github.com/Destructor-Ben/L3COSC-Maths-Website">GitHub</a
    >. It is also licensed under the MIT license.
  </p>

  <br />
  <p>
    Phobos was also initially created as a project for the Level 3 Computer Science course at my
    school, though I have plans to continue developing it and make it more than just a school
    project.
  </p>
</InfoPage>
